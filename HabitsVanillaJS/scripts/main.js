'use strict'

const capitals = function (word) {
	// Write your code here
  const newWord = word.split('');

  for (let i = 0; i < newWord.length; i++) {
    if (newWord[i].charAt() === newWord[i].toUpperCase()) {
      return newWord[i].indexOf();
    }
  }

  return [...newWord];
};
