// Loop operators
/*
for(<start condition>, <condition>, <step>) {
    <for body>
}
example :
for (let i = 0; i < 100; i++) {
    console.log(i)
}
for (let key in [3,2,1]) {
    console.log(key) // will print 0 1 2
}
for (let value of [3,2,1]) {
    console.log(value) // will print 3 2 1
}
// while cycle - pre-condition cycle
while (<condition>) {
    <while body>
}
// do ... while cycle post-condition cycle
do {
    <body>
} while (<condition>)
 */
