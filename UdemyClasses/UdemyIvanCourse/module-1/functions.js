function getMathResult(baseNumber, count) {
  if (typeof count !== 'number'  || count <= 0) {
    return baseNumber;
  }

  let string = '';

  for (let i = 0; i < count; i++) {
    if (i === count) {
      string += `${baseNumber * i}`;
    } else {
      string += `${baseNumber * i}---`;
    }
  }

  return string;
}

